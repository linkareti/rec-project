    @Action
    public void btnAction() {
    	
    	if(state){
    		flatButton1.setGradientBottom(FlatButton.GRADIENTBOTTOM_ON);
    		flatButton1.setGradientTop(FlatButton.GRADIENTTOP_ON);
    		flatButton1.setForeground(FlatButton.FOREGROUND_ON);
    		flatButton1.setBorderColor(FlatButton.COLORBORDER_ON);
    		state=false;
    	}else{
    		flatButton1.setGradientBottom(FlatButton.GRADIENTBOTTOM_OFF);
    		flatButton1.setGradientTop(FlatButton.GRADIENTTOP_OFF);
    		flatButton1.setForeground(FlatButton.FOREGROUND_OFF);
    		flatButton1.setBorderColor(FlatButton.COLORBORDER_OFF);
    		state=true;
    	}
    }
    

