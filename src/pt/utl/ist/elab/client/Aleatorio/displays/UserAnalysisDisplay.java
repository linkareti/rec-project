    private void houghThreshold3SliderStateChangedHandler(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_houghThreshold3SliderStateChangedHandler
        // Add your handling code here:
        houghThreshold3Slider.setBorder(new javax.swing.border.TitledBorder("Threshold3: " + String.valueOf(houghThreshold3Slider.getValue())));
        //((javax.swing.border.TitledBorder)houghThreshold3Slider.getBorder()).setTitle("Hough Threshold 3: " + String.valueOf(houghThreshold3Slider.getValue()) );
        BWThreshold = BWThresholdSlider.getValue();
        houghThreshold1 = houghThreshold1Slider.getValue();
        houghThreshold2 = houghThreshold2Slider.getValue();
        houghThreshold3 = houghThreshold3Slider.getValue();
        convolutionThreshold = convolutionThresholdSlider.getValue();
        radiusOfSpot = radiusOfSpotSlider.getValue();
        widthOfDie = widthOfDieSlider.getValue();
        try{numberOfExpectedDice = Integer.parseInt(numberOfExpectedDiceText.getText());}
            catch(NumberFormatException e){numberOfExpectedDice = NUMBER_OF_EXPECTED_DICE;}
        
        if (imagePanel!=null)
            imagePanel.setParams(BWThreshold, radiusOfSpot,
                                houghThreshold1, houghThreshold2,
                                houghThreshold3, convolutionThreshold,
                                widthOfDie, numberOfExpectedDice);
    }//GEN-LAST:event_houghThreshold3SliderStateChangedHandler

    private void fromExperimentButtonActionPerformedHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fromExperimentButtonActionPerformedHandler
        // Add your handling code here:
        if (userAnalysisSingleton.getImage()!=null)
        {
            imagePanel = new AnalysisPanel(userAnalysisSingleton.getImage());
            //imagePanel.setImage(userAnalysisSingleton.getImage());
            imagePanel.setPreferredSize(new java.awt.Dimension(imagePanel.imageWidth, imagePanel.imageHeight));
            imageScrollPane.setViewportView(imagePanel);
            BWThreshold = BWThresholdSlider.getValue();
            houghThreshold1 = houghThreshold1Slider.getValue();
            houghThreshold2 = houghThreshold2Slider.getValue();
            houghThreshold3 = houghThreshold3Slider.getValue();
            convolutionThreshold = convolutionThresholdSlider.getValue();
            radiusOfSpot = radiusOfSpotSlider.getValue();
            widthOfDie = widthOfDieSlider.getValue();
            try{numberOfExpectedDice = Integer.parseInt(numberOfExpectedDiceText.getText());}
            catch(NumberFormatException e){numberOfExpectedDice = NUMBER_OF_EXPECTED_DICE;}

            if (imagePanel!=null)
                imagePanel.setParams(BWThreshold, radiusOfSpot,
                                    houghThreshold1, houghThreshold2,
                                    houghThreshold3, convolutionThreshold,
                                    widthOfDie, numberOfExpectedDice);
            saveButton.setEnabled(true);
            originalButton.setEnabled(true);
            BWButton.setEnabled(true);
        }//if
        this.repaint();
    }//GEN-LAST:event_fromExperimentButtonActionPerformedHandler

