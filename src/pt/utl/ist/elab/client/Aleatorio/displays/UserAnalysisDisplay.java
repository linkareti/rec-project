    private void saveImageToFile(String fileName, java.awt.Image image, int quality)
    {
        try
        {
            java.awt.image.BufferedImage bImage = new  java.awt.image.BufferedImage(imagePanel.getWidth(), imagePanel.getHeight(), java.awt.image.BufferedImage.TYPE_4BYTE_ABGR);
            
            // Copy from Image to BufferedImage
            java.awt.Graphics2D g = (java.awt.Graphics2D)bImage.getGraphics();
            g.drawImage(image, 0, 0, this);
            g.dispose();
            
            java.io.FileOutputStream out = new java.io.FileOutputStream(fileName);
            com.sun.image.codec.jpeg.JPEGImageEncoder encoder = com.sun.image.codec.jpeg.JPEGCodec.createJPEGEncoder(out);
            com.sun.image.codec.jpeg.JPEGEncodeParam jep = com.sun.image.codec.jpeg.JPEGCodec.getDefaultJPEGEncodeParam(bImage);
            
            jep.setQuality((float)quality/100,true);
            encoder.setJPEGEncodeParam(jep);
            encoder.encode(bImage);
            
            out.close();
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
