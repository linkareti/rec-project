    /**The scope of this method is to get the real max column in respect of col:
     *for example if one needs to copy a column, one needs to know until which column
     *should it copy.
     *Example:
     *Copy col 1
     *X--X
     *X-X<-This is the maxAllowedColumn!!!(row 3)
     *XX
     *X
     *Since the third row has a connection with a component in the third column
     *this method will return 3. And one knows that must copy, or delete, etc.
     *until col 3. (remember that the copy method, starts to check for connections
     *at row+1!!)
     */
    private int getMaxAllowedColumn(int row, int col)
    {        
        int maxColAllowed=col;
        boolean abort=false;
        if(matrix[row+1][col]==null)
        {
            return (maxColAllowed+1);
        }
        for(int iCol=col;iCol<maxColumn+2;iCol++)
        {
            if(abort)
            {
                maxColAllowed=iCol;
                break;
            }
            for(int iRow=row+1;iRow<maxRow+1;iRow++)
            {
                abort=true;
                if(matrixWiring[iRow][iCol+1]!=null)
                {
                    if(matrixWiring[iRow][iCol+1].startsWith("<-"))
                    {
                        abort=false;
                        break;
                    }
                }
            }
        }
        return maxColAllowed;
    }
    
