    /**Deletes a block! If delColumn is selected, than don't test if it can delete
     *a block!
     */
    private void delBlock(int row, int col, boolean delColumn)
    {
        /**Can't delete a block, if it a comparison  block, and the block at row+1
         *isn't of comparison, or if the block at row-1 is null and the block at row
         *isn't of comparison!
         */
        if(isConnectionBlock(matrix[row][col])&&!delColumn)
        {
            if((matrix[row+1][col]!=null&&!isConnectionBlock(matrix[row+1][col])&&
                matrix[row-1][col]!=null&&!isConnectionBlock(matrix[row-1][col]))||
                (matrix[row+1][col]!=null&&!isConnectionBlock(matrix[row+1][col])&&
                matrix[row-1][col]==null))
            {
                javax.swing.JOptionPane.showMessageDialog(this, "N�o pode apagar o bloco de compara��o!", "Erro!", javax.swing.JOptionPane.ERROR_MESSAGE);            
                return;
            }
        }
        /**If the component add a connection with the component at its left, I don't
         *want to lose the connection!
         */
        boolean wasAddLeft=false;
        matrix[row][col]=null;
        if(matrixWiring[row][col]!=null)
        {
            if(matrixWiring[row][col].startsWith("<-"))
            {
                wasAddLeft=true;
            }
        }
        matrixWiring[row][col]=null;
        /**Bring all the components up
         */
        shiftRowUpFixedCol(row,col);
        if(wasAddLeft&&matrix[row][col-1]!=null)
        {
            matrixWiring[row][col]="<-";
        }        
        /**If it tried to shift up, and there weren't components to shift up, then 
         *try to move all the components at its right, one time to the left!
         *(matrixWiring[row][col+1].startsWith("<-")for the case of block of type 12 or 13)
         */
        if((matrix[row][col]==null&&matrixWiring[row][col+1]!=null&&
            matrixWiring[row][col+1].startsWith("<-"))||isColEmpty(col))
        {
            shiftColLeft(row,col);                    
            shiftedColsInDel=true;
        }
    }
            
