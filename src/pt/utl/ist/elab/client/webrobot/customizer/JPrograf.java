    private void jMenuItemPasteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemPasteActionPerformed
        /**Paste
         *Possibilities:
         *1-Paste Right(evt=null)
         *2-Paste Left(evt.getID()==-1)
         *3-Paste with right click(evt.getID()==-1)
         *4-Paste in selected cell(evt!=null)
         */
        
        int shiftUntil;
        
        /**If the ID of the event is !null and different than -1(paste with right click)
         *than we want to let the user be able to choose the pasting point!!
         */
        if(evt!=null&&evt.getID()!=-1)
        {   
            new CenterFrame(this,confPaste);
            confPaste.show();
            if(confPaste.isPasting()||confPaste.isCancel())
            {
                return;
            }
            rowToPaste=confPaste.getRowPaste();
            colToPaste=confPaste.getColumnPaste();
            if(matrix[rowToPaste][colToPaste]!=null)
            {
                javax.swing.JOptionPane.showMessageDialog(this, "N�o pode colar nesta posi��o", "Erro!", javax.swing.JOptionPane.ERROR_MESSAGE);            
                return;
            }
            if(matrix[rowToPaste-1][colToPaste]==null)
            {
                javax.swing.JOptionPane.showMessageDialog(this, "N�o pode colar nesta posi��o", "Erro!", javax.swing.JOptionPane.WARNING_MESSAGE);            
                return;
            }         
        }

        /**Get the matrixs from the CopyPaste*/
        matrixPaste=copyPaste.getMatrixData();
        matrixWiringPaste=copyPaste.getMatrixWiringData();

        /**If the event wasn't null then the user has choosen an empty row or 
         *column, below a column, then we want to shift the matrix like if we were         
         *introducing blocks at the right, for shiftUntil times. Of course if we 
         *are only pasting a column(or a single block), no shifting is done*/
        if(evt!=null)
        {
            shiftUntil=matrixPaste[0].length-1;
            for(int i=1;i<shiftUntil;i++)
            {
                shiftColRight(rowToPaste,colToPaste,1);
            }
        }
        /**If the event was null, means that the user has choosen paste right, or
         *and we have to paste in the right place (probably after all
         *the other columns...same rules of add block right or left, but for a 
         *block of matrixPaste dimension*/
        else
        {
            colToPaste=shiftColRight(rowToPaste,colToPaste-1,matrixPaste[0].length-1);            
        }
        
        /**Here we copy with the elements to paste, to the right place in matrix*/
        for(int iRow=1;iRow<matrixPaste.length;iRow++)
        {
            for(int iCol=1;iCol<matrixPaste[0].length;iCol++)
            {
                matrix[rowToPaste+iRow-1][colToPaste+iCol-1]=matrixPaste[iRow][iCol];
                matrixWiring[rowToPaste+iRow-1][colToPaste+iCol-1]=matrixWiringPaste[iRow][iCol];
            }
        }
        
        /**Update the maxRow and maxColumn, since we added new blocks, I multiply
         *per two just for security...maybe I lose a bit in performance, but 
         *certainly win in security!*/
        maxRow=maxRow+2*matrixPaste.length;
        maxColumn=maxColumn+2*matrixPaste[0].length;
        
        /**If the event wasn't null and different from -1, than the user has
         *choosen to add below a column, what implies that the block above
         *the pasting point must paint is bottom*/
        if(evt!=null&&evt.getID()!=-1)
        {
            matrix[rowToPaste-1][colToPaste].setPaintBottom(true);
            matrix[rowToPaste-1][colToPaste].revalidate();
            updateDisplay(true);
            /**We always have to recopy, so we don't lose the information,
             *this is because we don't want to point to the insert blocks,
             *but create new ones*/
            jMenuItemCopyActionPerformed(null);
        }
        else if(evt==null)
        {
            /**If the evt was null than it was a paste right operation and we
             *have to create a connection from the pasted cell to the cell in
             *the previous column...the other code is for the case of comps
             *of type 12 or 13*/
            if(matrix[rowToPaste][colToPaste-1]!=null)
            {
                matrix[rowToPaste][colToPaste-1].setPaintLeft(true);
                if(matrix[rowToPaste][colToPaste]!=null&&matrix[rowToPaste+1][colToPaste]!=null&&
                    (matrix[rowToPaste][colToPaste].getTipo()==12||matrix[rowToPaste][colToPaste].getTipo()==13)&&
                    (matrix[rowToPaste+1][colToPaste].getTipo()==12||matrix[rowToPaste+1][colToPaste].getTipo()==13))
                {
                    matrixWiring[rowToPaste][colToPaste]="<-1";
                }
                else
                {
                    matrixWiring[rowToPaste][colToPaste]="<-";
                }
                matrix[rowToPaste][colToPaste-1].revalidate();                            
            }
            updateDisplay(true);
            /**We always have to recopy, so we don't lose the information,
             *this is because we don't want to point to the inserted blocks,
             *(having only references) but create new ones*/
            jMenuItemCopyActionPerformed(null);
        }
        else 
        {
            updateDisplay(true);
            /**We always have to recopy, so we don't lose the information,
             *this is because we don't want to point to the inserted blocks,
             *but create new ones*/            
            jMenuItemCopyActionPerformed(null);
        }
    }//GEN-LAST:event_jMenuItemPasteActionPerformed

    private void jMenuItemCopyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemCopyActionPerformed
        /**Copy*/
        
        /**Why tempMatrix? Because we have to create new comps, otherwise we'll
         *keep pointing to the existing components and will lose them*/
        pt.utl.ist.elab.client.webrobot.customizer.Comps.Block[][] tempMatrix=matrixToCopy;
        String[][] tempMatrixWiring=matrixWiringToCopy;
        
        /**If the evt wasn't null(comes from the paste) and different from -2
         *(from copy column in the block's selection options), the user wants
         *to copy a specific column*/
        if(evt!=null&&evt.getID()!=-2)
        {
            new CenterFrame(this,confCopy);
            confCopy.show();                        
            if(confCopy.isCopying()||confCopy.isCancel())
            {
                return;
            }
            if(matrix[confCopy.getRowCopy()][confCopy.getColumnCopy()]==null)
            {
                javax.swing.JOptionPane.showMessageDialog(this, "A coluna e a linha escolhida, n�o permitem que se efectue esta opera��o!", "Erro!", javax.swing.JOptionPane.ERROR_MESSAGE);            
                return;                
            }
        }
        /**As said before the event=null, implies that it comes from paste, to recopy
         *the elements so we don't lose them. Meaning if we just recopy the same
         *matrix, we'll move the components that where previous pasted, because
         *it would copy only the reference to the objects, and not the objects
         *...*/
        else if(evt==null)
        {
            for(int iRow=0;iRow<matrixToCopy.length;iRow++)
            {
                for(int iCol=0;iCol<matrixToCopy[0].length;iCol++)
                {
                    if(matrixToCopy[iRow][iCol]!=null)
                    {
                        pt.utl.ist.elab.client.webrobot.customizer.Comps.Block copyBlock=createNewBlock(matrixToCopy[iRow][iCol].getTipo(),matrixToCopy[iRow][iCol].getDataModel());
                        addCompEvents(copyBlock);
                        /**If the matrix wasn't null for this cell, and the returned
                         *block is null, then it was a connection block(horLine)
                         *for sure*/
                        if(copyBlock==null)
                        {
                            copyBlock=new pt.utl.ist.elab.client.webrobot.customizer.Comps.HorLine();
                        }
                        tempMatrix[iRow][iCol]=copyBlock;
                    }
                    else
                    {
                        tempMatrix[iRow][iCol]=null;
                    }               
                    /**Copies the connection matrix contents*/
                    tempMatrixWiring[iRow][iCol]=matrixWiringToCopy[iRow][iCol];
                }
            }
            /**The new copy matrix was created with "fresh" components and
             *it's ready for another paste!!*/
            copyPaste.addData(tempMatrix);
            copyPaste.addData(tempMatrixWiring);
            return;
        }
        /**If the event wasn't null we have two options: it came from copy colum
         *in the block options evt.getID()==-2, or it came from the copy UI
         *If it came from the copy UI, one must actualize the GLOBAL variables
         *rowToCopyIni and colToCopyIni*/
        if(evt!=null)
        {
            if(evt.getID()!=-2)
            {
                rowToCopyIni=confCopy.getRowCopy();                
                colToCopyIni=confCopy.getColumnCopy();
            }
        }

        /**We want to copy all the links that this column has in all the rows
         *BELOW the selected one!!!*/
        colToCopyEnd=getMaxAllowedColumn(rowToCopyIni,colToCopyIni);
        /**Get the last non null row, we don't want to copy until maxRow(which is
         *almost for sure different from the last non null row...I do this 
         *for matrix dimension problems*/
        rowToCopyEnd=getLastNonNullRow();
        /**Security check*/
        if(rowToCopyIni==0)
        {
            return;
        }
        
        /**Creates two new matrixs with correct dimensions, then the neading of
         *the accuracy in the last row (getLastNonNullRow), remember that maxRow
         *is only for performance reasons so that we don't have to huge cycles
         *,but is only an approximate number (always bigger than the real maximum row
         *populated*/
        matrixToCopy = new pt.utl.ist.elab.client.webrobot.customizer.Comps.Block[rowToCopyEnd-rowToCopyIni+1][colToCopyEnd-colToCopyIni+1];
        matrixWiringToCopy = new String[rowToCopyEnd-rowToCopyIni+1][colToCopyEnd-colToCopyIni+1];
        int rowCp=1;
        int colCp=1;
        
        /**Copy the matrix and create new block...already explained before*/
        for(int iRow=rowToCopyIni;iRow<rowToCopyEnd;iRow++)
        {    
            colCp=1;
            for(int iCol=colToCopyIni;iCol<colToCopyEnd;iCol++)
            {
                if(matrix[iRow][iCol]!=null)
                {
                    pt.utl.ist.elab.client.webrobot.customizer.Comps.Block copyBlock=createNewBlock(matrix[iRow][iCol].getTipo(),matrix[iRow][iCol].getDataModel());
                    addCompEvents(copyBlock);
                    if(copyBlock==null)
                    {
                        copyBlock=new pt.utl.ist.elab.client.webrobot.customizer.Comps.HorLine();
                    }
                    matrixToCopy[rowCp][colCp]=copyBlock;
                }
                else
                {
                    matrixToCopy[rowCp][colCp]=null;
                }                    
                matrixWiringToCopy[rowCp][colCp]=matrixWiring[iRow][iCol];
                colCp++;
                /**if this column, has another one at its left, I don't want to
                 *copy the connections to this column!!
                 */                 
                if(colCp==1)
                {
                    matrixWiringToCopy[rowCp][colCp]=null;
                }
                /**Don't copy connections that are at the right of the starting block
                 *in the column selected to copy
                 */
                if(iRow==rowToCopyIni)
                {
                    break;
                }
            }
            rowCp++;
        }
        copyPaste.addData(matrixToCopy);
        copyPaste.addData(matrixWiringToCopy);
    }//GEN-LAST:event_jMenuItemCopyActionPerformed

