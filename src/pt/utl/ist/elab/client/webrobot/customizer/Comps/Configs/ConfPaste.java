    private void jButtonOkMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonOkMousePressed
        int cp=-1;
        int rp=-1;
        try
        {
            cp = new Integer(jTextFieldCol.getText()).intValue();
        }
        catch(NumberFormatException nfe)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, introduza um n�mero inteiro", "Erro!", javax.swing.JOptionPane.ERROR_MESSAGE);            
            jTextFieldCol.selectAll();
            jTextFieldCol.requestFocus();
            return;
        }
        try
        {
            rp = new Integer(jTextFieldRow.getText()).intValue();
        }
        catch(NumberFormatException nfe)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, introduza um n�mero inteiro", "Erro!", javax.swing.JOptionPane.ERROR_MESSAGE);            
            jTextFieldRow.selectAll();
            jTextFieldRow.requestFocus();
            return;
        }
        if(cp<1)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, introduza um n�mero inteiro maior que zero", "Erro!", javax.swing.JOptionPane.ERROR_MESSAGE);            
            jTextFieldCol.selectAll();
            jTextFieldCol.requestFocus();
            return;
        }
        if(rp<1)
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, introduza um n�mero inteiro maior que zero", "Erro!", javax.swing.JOptionPane.ERROR_MESSAGE);            
            jTextFieldRow.selectAll();
            jTextFieldRow.requestFocus();
            return;
        }
        setColumnPaste(cp);
        setRowPaste(rp);
        setCancel(false);
        setPasting(false);
        closeDialog(null);
    }//GEN-LAST:event_jButtonOkMousePressed
    
