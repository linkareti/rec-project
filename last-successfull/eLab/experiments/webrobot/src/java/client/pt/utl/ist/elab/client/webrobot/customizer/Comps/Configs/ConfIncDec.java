	public void readModel() {
		jComboBoxD1.setSelectedItem(model.getD1());
		if (model.getFlag() == 0) {
			jRadioButtonInc.setSelected(true);
			jRadioButtonDec.setSelected(false);
			jLabel1.setIcon(new javax.swing.ImageIcon(getClass().getResource(
					"/pt/utl/ist/elab/client/webrobot/customizer/Comps/Icons/incdec+.gif")));
		} else {
			jRadioButtonInc.setSelected(false);
			jRadioButtonDec.setSelected(true);
			jLabel1.setIcon(new javax.swing.ImageIcon(getClass().getResource(
					"/pt/utl/ist/elab/client/webrobot/customizer/Comps/Icons/incdec-.gif")));
		}
	}

